#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	int		c;
	char	*str;

	c = 0;
	str = (char *)s;
	while (n > 0)
	{
		str[n - 1] = c;
		n--;
	}
	return (str);
}
