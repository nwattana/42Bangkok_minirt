#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	s;
	int		check;

	check = 1;
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		check = 0;
	}
	else if (n < 0)
	{
		write (fd, "-", 1);
		n = n / (-1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	s = n % 10 + '0';
	if (check)
		write(fd, &s, 1);
}
