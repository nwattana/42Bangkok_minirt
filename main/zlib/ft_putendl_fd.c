#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	ch;

	ch = '\n';
	while (*s != '\0')
	{
		write(fd, s, 1);
		s++;
	}
	ft_putchar_fd(ch, fd);
}
